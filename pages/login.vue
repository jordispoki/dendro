<script setup lang="ts">
definePageMeta({ layout: false })

const { loggedIn } = useUserSession()
const route = useRoute()

if (loggedIn.value) {
  navigateTo('/')
}

const email = ref('')
const magicLinkSent = ref(false)
const isSending = ref(false)
const error = ref('')
const devMagicLink = ref('')

const errorMessages: Record<string, string> = {
  github: 'GitHub sign-in failed. Please try again.',
  google: 'Google sign-in failed. Please try again.',
  'no-email': 'No email associated with your account.',
  'invalid-token': 'Invalid or expired magic link.',
  'expired-token': 'Magic link has expired. Please request a new one.',
}

onMounted(() => {
  const errorParam = route.query.error as string
  if (errorParam && errorMessages[errorParam]) {
    error.value = errorMessages[errorParam]
  }
})

async function sendMagicLink() {
  if (!email.value || isSending.value) return
  isSending.value = true
  error.value = ''

  try {
    const result = await $fetch<{ message: string; magicLink?: string }>('/api/auth/magic-link', {
      method: 'POST',
      body: { email: email.value },
    })
    magicLinkSent.value = true
    if (result.magicLink) {
      devMagicLink.value = result.magicLink
    }
  } catch (err: any) {
    error.value = err?.data?.message || 'Failed to send magic link.'
  } finally {
    isSending.value = false
  }
}
</script>

<template>
  <div class="min-h-screen bg-gradient-to-br from-indigo-50 to-white dark:from-gray-950 dark:to-gray-900 flex items-center justify-center p-4">
    <div class="w-full max-w-sm">
      <!-- Logo / Brand -->
      <div class="text-center mb-8">
        <div class="inline-flex items-center justify-center w-14 h-14 bg-indigo-600 rounded-2xl mb-4 shadow-lg">
          <svg class="w-8 h-8 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="1.5" d="M9 20l-5.447-2.724A1 1 0 013 16.382V5.618a1 1 0 011.447-.894L9 7m0 13l6-3m-6 3V7m6 10l4.553 2.276A1 1 0 0021 18.382V7.618a1 1 0 00-.553-.894L15 4m0 13V4m0 0L9 7" />
          </svg>
        </div>
        <h1 class="text-2xl font-bold text-gray-900 dark:text-white">Dendro</h1>
        <p class="text-sm text-gray-500 dark:text-gray-400 mt-1">Explore ideas as a conversation dendro</p>
      </div>

      <div class="bg-white dark:bg-gray-900 rounded-2xl shadow-xl border border-gray-100 dark:border-gray-800 p-6">
        <!-- Error -->
        <div
          v-if="error"
          class="mb-4 px-3 py-2.5 rounded-lg bg-red-50 dark:bg-red-900/20 text-sm text-red-600 dark:text-red-400"
        >
          {{ error }}
        </div>

        <!-- OAuth buttons -->
        <div class="space-y-3 mb-6">
          <a
            href="/api/auth/github"
            class="flex items-center justify-center gap-3 w-full px-4 py-2.5 border border-gray-200 dark:border-gray-700 rounded-xl text-sm font-medium text-gray-700 dark:text-gray-200 hover:bg-gray-50 dark:hover:bg-gray-800 transition-colors"
          >
            <svg class="w-5 h-5" viewBox="0 0 24 24" fill="currentColor">
              <path d="M12 0C5.374 0 0 5.373 0 12c0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23A11.509 11.509 0 0112 5.803c1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576C20.566 21.797 24 17.3 24 12c0-6.627-5.373-12-12-12z" />
            </svg>
            Continue with GitHub
          </a>

          <a
            href="/api/auth/google"
            class="flex items-center justify-center gap-3 w-full px-4 py-2.5 border border-gray-200 dark:border-gray-700 rounded-xl text-sm font-medium text-gray-700 dark:text-gray-200 hover:bg-gray-50 dark:hover:bg-gray-800 transition-colors"
          >
            <svg class="w-5 h-5" viewBox="0 0 24 24">
              <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
              <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
              <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
              <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
            </svg>
            Continue with Google
          </a>
        </div>

        <!-- Divider -->
        <div class="relative mb-6">
          <div class="absolute inset-0 flex items-center">
            <div class="w-full border-t border-gray-200 dark:border-gray-700" />
          </div>
          <div class="relative flex justify-center text-xs text-gray-400 dark:text-gray-500">
            <span class="bg-white dark:bg-gray-900 px-2">or continue with email</span>
          </div>
        </div>

        <!-- Magic link form -->
        <div v-if="!magicLinkSent">
          <div class="flex gap-2">
            <input
              v-model="email"
              type="email"
              placeholder="you@example.com"
              class="flex-1 px-3 py-2.5 text-sm border border-gray-200 dark:border-gray-700 rounded-xl bg-white dark:bg-gray-800 text-gray-900 dark:text-white placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-indigo-500"
              @keydown.enter="sendMagicLink"
            />
            <button
              :disabled="!email || isSending"
              class="px-4 py-2.5 text-sm font-medium bg-indigo-600 hover:bg-indigo-700 disabled:opacity-50 text-white rounded-xl transition-colors"
              @click="sendMagicLink"
            >
              {{ isSending ? '…' : 'Send' }}
            </button>
          </div>
        </div>

        <div v-else class="text-center">
          <div class="text-sm text-gray-600 dark:text-gray-300 mb-2">
            Magic link sent to <strong>{{ email }}</strong>
          </div>
          <div class="text-xs text-gray-400">Check your email and click the link to sign in.</div>
          <!-- Dev mode: show clickable link -->
          <div v-if="devMagicLink" class="mt-3 p-2 bg-yellow-50 dark:bg-yellow-900/20 rounded-lg">
            <div class="text-xs text-yellow-700 dark:text-yellow-300 font-medium mb-1">Dev mode — click to sign in:</div>
            <a :href="devMagicLink" class="text-xs text-indigo-600 dark:text-indigo-400 underline break-all">{{ devMagicLink }}</a>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>
